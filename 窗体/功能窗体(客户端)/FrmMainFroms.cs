                        {
                            #region 如果没有音乐则将音乐下载到本地
                            Dictionary<string, object> dict = new Dictionary<string, object>();
                            dict.Add("@guid", Convert.ToString(row[0]["guid"]));


                            var dt_MusicByte = SqlHelper.ExecuteQuery($@"select * from MusicByte where Music_guid = @guid", dict);
                            if (dt_MusicByte != null && dt_MusicByte.Rows.Count > 0)
                            {
                                var dr = dt_MusicByte.Rows[0];
                                var Photo = dr["Music_byte"];
                                if (Photo != null)
                                {
                                    byte_File(PlayMusics.FilePath, (byte[])Photo);
                                }

                                btnStart_Click_fangfa();
                            }
                            #endregion
                        }
                    }

                    if (frm_FavoriteMusic != null)
                        this.frm_FavoriteMusic.LoadData(this.File_Hash);

                    if (frm_HistoryPlay != null)
                        this.frm_HistoryPlay.LoadData(this.File_Hash);
                }
                else//暂停
                {
                    classPause.Pause();
                    Program.btnStart_int = 0;
                    Program.btnStart_switch = true;
                    btnStart.ImageOptions.Image = Image.FromFile(AppDomain.CurrentDomain.BaseDirectory + "\\Resources\\暂停.png");//赋值继续按钮图标
                    timer.Stop();//关闭计时

                    if (frm_FavoriteMusic != null)
                        frm_FavoriteMusic.LoadData(this.File_Hash);

                    if (frm_HistoryPlay != null)
                        frm_HistoryPlay.LoadData(this.File_Hash);
                }
            }
            else
            {
                is_Enable_Music();
            }
        }

