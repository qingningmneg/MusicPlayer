        /// <summary>
        /// 创建文件
        /// </summary>
        public void byte_File(string file_path_hast_name_suffix, byte[] path_byte)
        {
            FileStream fs = new FileStream((file_path_hast_name_suffix), FileMode.Create, FileAccess.Write);

