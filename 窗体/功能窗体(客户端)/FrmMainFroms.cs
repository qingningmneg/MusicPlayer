        /// <summary>
        /// 读取时间
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void timer_Tick(object sender, EventArgs e)
        {
            int_timer++;
            if (int_timer > int_PlayMusic_millisecond)
            {
                timer.Stop();
                btnNextSong_Click(null, null);
            }
            var minute = (int_timer / 60).ToString();
            if (minute.Length == 1)
            {
                minute = "0" + minute;
            }

            var second = (int_timer % 60).ToString();
            if (second.Length == 1)
            {
                second = "0" + second;
            }

            label1.Text = minute + ":" + second;

            zoomTrackBarControl.Properties.Maximum = int_PlayMusic_millisecond;

            zoomTrackBarControl.Value = int_timer;
        }

